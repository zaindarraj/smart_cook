package com.example.smartcook.classes;

public class Reciepe {

    String name;
    String benf;
    String how_to;
    String image;

    public String getName() {
        return name;
    }

    public String getBenf() {
        return benf;
    }

    public String getImage() {
        return image;
    }

    public String getHow_to() {
        return how_to;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setBenf(String benf) {
        this.benf = benf;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public void setHow_to(String how_to) {
        this.how_to = how_to;
    }
}
